#!/usr/bin/env python3
"""
repair_vault.py — deterministic reconcile/fix pass for an existing vault.

The validator (validate_vault.py) DETECTS issues; this script FIXES the
mechanically-fixable ones in place. It never deletes content — it only
rewrites specific frontmatter fields, adds missing reverse wikilinks, and
backticks render-breaking tokens in bodies. `last_updated` is bumped on any
file it changes.

Why this exists: template/spec fixes only apply to newly generated files.
A vault generated by an older skill version stays broken forever unless a
repair pass reconciles it. Without this, the v0.6.3 validation gate would
keep `_state.json` at "partial" indefinitely on legacy errors.

Fixes (deterministic):
  1. Meta/archive/log tag normalization → the canonical 5-distinct tuple
     for that file type (project tag preserved if valid, else from --project).
  2. slug == filename stem minus the type prefix (verbatim).
  3. Asymmetric wikilinks: if A lists B in linked_nodes and B exists but
     does not list A, add A to B.
  4. Archive content-type: force position-4 to `summary`.
  5. Markdown safety: backtick bare <tag>-like tokens in bodies (outside
     code fences / existing inline code).

Flagged but NOT auto-fixed (reported for manual/LLM handling):
  - node_/entity_ files with genuine duplicate domain tags (needs a
    semantic re-tag; rare after v0.6.2).

Stdout: JSON {summary, fixed[], flagged[]}
Exit codes:
    0 — OK (with or without fixes applied)
    1 — argument error
    2 — I/O error
    3 — vault path not found
"""

import argparse
import json
import re
import sys
from pathlib import Path

TYPE_DIR = {"node": "nodes", "entity": "entities",
            "chat": "chats", "log": "logs", "archive": "archives"}
WIKILINK_RE = re.compile(r"\[\[([^\]|]+?)(?:\|[^\]]+)?\]\]")
RAW_ANGLE_RE = re.compile(r"(?<!`)<[a-zA-Z][a-zA-Z0-9_-]*>")
CODE_FENCE_RE = re.compile(r"^\s*```")

CANON_TAGS = {
    "_INDEX":  ["vault-index", "navigation", "knowledge-management",
                "meta", "vault-ops"],
    "_RECENT": ["recent-activity", "navigation", "knowledge-management",
                "meta", "vault-ops"],
}
# archive / log canonical domain part; project (pos 5) filled per-file
ARCHIVE_CANON = ["archive", "history", "knowledge-management", "summary"]
LOG_CANON = ["daily-log", "knowledge-management", "vault-ops", "summary"]


def split_fm(text):
    """Return (fm_lines:list, body:str) or (None, text) if no frontmatter."""
    if not text.startswith("---"):
        return None, text
    end = text.find("\n---", 3)
    if end == -1:
        return None, text
    fm = text[3:end].strip("\n").splitlines()
    body = text[end + 4:]
    return fm, body


def fm_get(fm_lines, key):
    """Read a scalar or inline/block list value from frontmatter lines."""
    for i, ln in enumerate(fm_lines):
        m = re.match(rf"^{re.escape(key)}:\s*(.*)$", ln)
        if m:
            val = m.group(1).strip()
            if val.startswith("["):
                inner = val.strip("[]").strip()
                return [x.strip().strip('"') for x in inner.split(",")
                        if x.strip()]
            if val == "":
                items = []
                j = i + 1
                while j < len(fm_lines) and re.match(r"^\s*-\s", fm_lines[j]):
                    items.append(fm_lines[j].strip()[1:].strip().strip('"'))
                    j += 1
                return items if items else ""
            return val.strip('"')
    return None


def fm_set_scalar(fm_lines, key, value):
    for i, ln in enumerate(fm_lines):
        if re.match(rf"^{re.escape(key)}:\s*", ln):
            fm_lines[i] = f"{key}: {value}"
            return True
    fm_lines.append(f"{key}: {value}")
    return True


def fm_set_list(fm_lines, key, items):
    """Replace key's value (scalar/inline/block) with a block list."""
    start = None
    for i, ln in enumerate(fm_lines):
        if re.match(rf"^{re.escape(key)}:\s*", ln):
            start = i
            break
    block = [f"{key}:"] + [f"  - {it}" for it in items]
    if start is None:
        fm_lines.extend(block)
        return
    end = start + 1
    # consume an existing block list
    while end < len(fm_lines) and re.match(r"^\s*-\s", fm_lines[end]):
        end += 1
    fm_lines[start:end] = block


def project_tag(existing_tags, fallback):
    """Pick a valid project tag from position 5 if present and not generic."""
    if isinstance(existing_tags, list) and len(existing_tags) == 5:
        cand = existing_tags[4]
        if cand and cand not in ("meta", "summary", "vault"):
            return cand
    return fallback


def repair_file(p, vault, all_names, fixed, flagged, project_fallback):
    rel = str(p.relative_to(vault))
    try:
        text = p.read_text(encoding="utf-8", errors="replace")
    except OSError as e:
        flagged.append({"file": rel, "issue": f"unreadable: {e}"})
        return None
    fm, body = split_fm(text)
    if fm is None:
        return None  # README / _state.json — nothing to repair

    name = p.stem
    changed = []
    ftype = fm_get(fm, "type") or ""
    tags = fm_get(fm, "tags")
    tags = tags if isinstance(tags, list) else []

    # 1. tag normalization for meta / archive / log
    if name in CANON_TAGS:
        want = CANON_TAGS[name]
        if tags != want:
            fm_set_list(fm, "tags", want)
            changed.append("tags->canonical")
    elif ftype == "archive":
        want = ARCHIVE_CANON + [project_tag(tags, project_fallback)]
        if tags != want:
            fm_set_list(fm, "tags", want)
            changed.append("tags->archive-canonical")
    elif ftype == "log":
        want = LOG_CANON + [project_tag(tags, project_fallback)]
        if tags != want:
            fm_set_list(fm, "tags", want)
            changed.append("tags->log-canonical")
    elif ftype in ("node", "entity"):
        if len(tags) == 5 and len(set(tags)) < 5:
            flagged.append({"file": rel,
                            "issue": f"duplicate domain tag(s) {tags} — "
                                     f"needs semantic re-tag (not auto-fixed)"})

    # 2. slug == filename tail
    slug = fm_get(fm, "slug")
    expect = None
    if name in ("_INDEX", "_RECENT"):
        expect = name[1:]            # INDEX / RECENT
    else:
        for pfx in ("_archive_", "node_", "entity_", "chat_", "log_"):
            if name.startswith(pfx):
                expect = name[len(pfx):]
                break
    if expect is not None and slug is not None and slug != expect:
        fm_set_scalar(fm, "slug", expect)
        changed.append(f"slug->{expect}")

    # 4. archive content-type → summary (position 4)
    if ftype == "archive":
        t2 = fm_get(fm, "tags")
        if isinstance(t2, list) and len(t2) == 5 and t2[3] != "summary":
            t2[3] = "summary"
            fm_set_list(fm, "tags", t2)
            changed.append("archive-content-type->summary")

    # 5. markdown safety in body
    new_body_lines = []
    in_fence = False
    body_changed = False
    for ln in body.splitlines():
        if CODE_FENCE_RE.match(ln):
            in_fence = not in_fence
            new_body_lines.append(ln)
            continue
        if in_fence:
            new_body_lines.append(ln)
            continue

        def _wrap(m):
            return f"`{m.group(0)}`"
        # don't touch tokens already inside inline code
        segs = re.split(r"(`[^`]*`)", ln)
        for k in range(0, len(segs), 2):
            if RAW_ANGLE_RE.search(segs[k]):
                segs[k] = RAW_ANGLE_RE.sub(_wrap, segs[k])
        nl = "".join(segs)
        if nl != ln:
            body_changed = True
        new_body_lines.append(nl)
    if body_changed:
        body = "\n".join(new_body_lines)
        if not body.endswith("\n"):
            body += "\n"
        changed.append("markdown-safety")

    if changed:
        # bump last_updated to today (date only; preserve if has time)
        from datetime import datetime, timezone
        now = datetime.now(timezone.utc).astimezone().isoformat(
            timespec="seconds")
        fm_set_scalar(fm, "last_updated", now)
        new_text = "---\n" + "\n".join(fm) + "\n---" + body
        try:
            p.write_text(new_text, encoding="utf-8")
        except OSError as e:
            flagged.append({"file": rel, "issue": f"write failed: {e}"})
            return None
        fixed.append({"file": rel, "changes": changed})
    return fm


def repair_links(vault, all_names, fixed):
    """Add missing reverse links so every A->B has B->A (B must exist)."""
    files = {}
    for p in sorted(vault.rglob("*.md")):
        if any(part.startswith(".") for part in p.relative_to(vault).parts):
            continue
        fm, body = split_fm(p.read_text(encoding="utf-8", errors="replace"))
        if fm is None:
            continue
        ln = fm_get(fm, "linked_nodes")
        ln = ln if isinstance(ln, list) else []
        targets = []
        for raw in ln:
            for t in WIKILINK_RE.findall(str(raw)):
                targets.append(t.strip())
        files[p] = {"name": p.stem, "fm": fm, "body": body,
                    "targets": targets}

    name_to_path = {v["name"]: p for p, v in files.items()}
    for p, v in files.items():
        for tgt in v["targets"]:
            base = tgt.split("/")[-1]
            tp = name_to_path.get(base)
            if not tp:
                continue
            tv = files[tp]
            if v["name"] not in tv["targets"]:
                tv["targets"].append(v["name"])
                tv["_dirty"] = True

    from datetime import datetime, timezone
    for p, v in files.items():
        if not v.get("_dirty"):
            continue
        fm = v["fm"]
        links = [f'"[[{t}]]"' for t in dict.fromkeys(v["targets"])]
        fm_set_list(fm, "linked_nodes", links)
        now = datetime.now(timezone.utc).astimezone().isoformat(
            timespec="seconds")
        fm_set_scalar(fm, "last_updated", now)
        p.write_text("---\n" + "\n".join(fm) + "\n---" + v["body"],
                     encoding="utf-8")
        fixed.append({"file": str(p.relative_to(vault)),
                      "changes": ["added reverse wikilink(s)"]})


def materialize_stubs(vault, project, fixed):
    """Create status:draft stubs for dangling targets referenced by >=3
    distinct files. Covers BOTH node_ and entity_ targets (v0.6.7: the
    >=3-ref guardrail previously only produced entity stubs). The distiller
    fleshes these out the next time the concept appears in a session."""
    from datetime import datetime, timezone
    import random

    names = set()
    refs = {}   # target basename -> set(referring rel-paths)
    for p in sorted(vault.rglob("*.md")):
        if any(x.startswith(".") for x in p.relative_to(vault).parts):
            continue
        names.add(p.stem)
        txt = p.read_text(encoding="utf-8", errors="replace")
        fm, body = split_fm(txt)
        scan = txt if fm is None else body
        rel = str(p.relative_to(vault))
        for tgt in WIKILINK_RE.findall(scan):
            base = tgt.strip().split("/")[-1]
            refs.setdefault(base, set()).add(rel)
        if fm is not None:
            ln = fm_get(fm, "linked_nodes")
            for raw in (ln if isinstance(ln, list) else []):
                for t in WIKILINK_RE.findall(str(raw)):
                    refs.setdefault(t.strip().split("/")[-1],
                                    set()).add(rel)

    now = datetime.now(timezone.utc).astimezone().isoformat(
        timespec="seconds")
    for base, referrers in sorted(refs.items()):
        if base in names or len(referrers) < 3:
            continue
        if base.startswith("node_"):
            sub, slug = "nodes", base[len("node_"):]
        elif base.startswith("entity_"):
            sub, slug = "entities", base[len("entity_"):]
        else:
            continue  # only node_/entity_ targets get stubs
        if not slug:
            continue
        sid = "".join(random.choice("0123456789abcdefghijklmnopqrstuvwxyz")
                      for _ in range(3))
        ftype = "node" if sub == "nodes" else "entity"
        ct = "concept" if ftype == "node" else "entity"
        title = slug.replace("-", " ").replace("_", " ").strip().capitalize()
        d = vault / sub
        d.mkdir(parents=True, exist_ok=True)
        fp = d / f"{base}.md"
        if fp.exists():
            continue
        stub = (
            "---\n"
            f"id: {datetime.now().strftime('%Y%m%d-%H%M%S')}-{sid}\n"
            f"type: {ftype}\n"
            f'title: "{title}"\n'
            f"slug: {slug}\n"
            f"tags: [unfiled, knowledge-management, vault-ops, {ct}, "
            f"{project}]\n"
            "status: draft\n"
            "linked_nodes: []\n"
            "sources: []\n"
            f"created: {now}\n"
            f"last_updated: {now}\n"
            "---\n\n"
            f"# {title}\n\n"
            f"> **status: draft** — auto-created because {len(referrers)} "
            f"files reference `[[{base}]]` but no file existed. The "
            f"distiller will flesh this out the next time the concept "
            f"appears in a session. Safe to edit or delete.\n\n"
            "TODO: define this concept.\n"
        )
        fp.write_text(stub, encoding="utf-8")
        fixed.append({"file": str(fp.relative_to(vault)),
                      "changes": [f"created status:draft stub "
                                  f"(>=3-ref: {len(referrers)} referrers)"]})


def main():
    ap = argparse.ArgumentParser(description=__doc__.splitlines()[1])
    ap.add_argument("--vault", required=True)
    ap.add_argument("--project", default="personal",
                    help="fallback project tag when one can't be derived")
    ap.add_argument("--links-only", action="store_true",
                    help="run ONLY the deterministic graph-integrity pass "
                         "(reverse-link symmetrization + >=3-ref stub "
                         "materialization). Skips tag/slug/markdown repairs. "
                         "The linker sub-agent calls this as its final, "
                         "mechanical step so the validation gate never sees "
                         "asymmetric links — no per-run repair churn.")
    ap.add_argument("--format", choices=["json", "text"], default="json")
    args = ap.parse_args()

    vault = Path(args.vault).expanduser()
    if not vault.is_dir():
        print(f"Vault not found: {vault}", file=sys.stderr)
        sys.exit(3)

    all_names = {p.stem for p in vault.rglob("*.md")
                 if not any(x.startswith(".")
                            for x in p.relative_to(vault).parts)}
    fixed, flagged = [], []
    try:
        if not args.links_only:
            for p in sorted(vault.rglob("*.md")):
                if any(x.startswith(".")
                       for x in p.relative_to(vault).parts):
                    continue
                repair_file(p, vault, all_names, fixed, flagged,
                            args.project)
        # graph-integrity pass (always — also the whole job of --links-only):
        # stubs first so new stub files become valid link targets, then
        # symmetrize so every A->B has B->A.
        materialize_stubs(vault, args.project, fixed)
        all_names = {p.stem for p in vault.rglob("*.md")
                     if not any(x.startswith(".")
                                for x in p.relative_to(vault).parts)}
        repair_links(vault, all_names, fixed)
    except OSError as e:
        print(f"I/O error: {e}", file=sys.stderr)
        sys.exit(2)

    result = {
        "summary": {"files_fixed": len({f['file'] for f in fixed}),
                    "total_changes": len(fixed),
                    "flagged_for_manual": len(flagged)},
        "fixed": fixed,
        "flagged": flagged,
    }
    if args.format == "text":
        s = result["summary"]
        print(f"repair: {s['files_fixed']} files fixed, "
              f"{s['flagged_for_manual']} flagged for manual review")
        for f in fixed:
            print(f"  FIXED {f['file']}: {', '.join(f['changes'])}")
        for f in flagged:
            print(f"  FLAG  {f['file']}: {f['issue']}")
    else:
        json.dump(result, sys.stdout, indent=2)
        sys.stdout.write("\n")
    sys.exit(0)


if __name__ == "__main__":
    main()
